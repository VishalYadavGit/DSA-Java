package SpaceTime;

public class LinearSpaceTime {
    public static void main(String args[]){
        int[] arr  = new int[]{1,2,3,4,5,6};
        for(int i=0;i< arr.length;i++){
            System.out.println(arr[i]); //This program has a linear time complexity i.e O(n)
        }
        //This program has space complexity of O(1)
    }

}
